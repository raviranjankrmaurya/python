# Variable = A container for a value (string ,integer, float, boolean, etc)
#            a variable behave as if it was the value it contains

#Strings
first_name = "Raviranjan"
food = "momos"
email = "ravi@ravi.com"

print(f"Hello {first_name}")
print(f"You like {food}")
print(f"your email is {email}")

#Integer
age = 20;
quantity = 4;
no_of_student = 76;

print(f"you are {age} years old.")
print(f"you are buying {quantity} items")
print(f"{no_of_student} students in class")

#float
price = 10.34;
print(f"price is ₹{price}")

#boolean
isPresent = True;
print(f"Ravi is present in class :{isPresent}")

